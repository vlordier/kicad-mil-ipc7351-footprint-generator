"""KiCad .kicad_mod footprint file exporter.

Generates KiCad v6/v7/v8 compatible footprint files from FootprintResult.
"""

from dataclasses import dataclass, field
from typing import Optional
from pathlib import Path

from ..core.ipc7351 import FootprintResult


class KiCadModExporter:
    """Export a FootprintResult to KiCad .kicad_mod format."""

    def __init__(self, result: FootprintResult):
        self.result = result

    def export(self, output_path: str | Path) -> None:
        """Write the .kicad_mod file."""
        output_path = Path(output_path)
        content = self._generate()
        output_path.write_text(content)

    def to_string(self) -> str:
        """Return the .kicad_mod content as a string."""
        return self._generate()

    def _generate(self) -> str:
        """Build the .kicad_mod s-expression string."""
        lines = [f"(footprint \"{self._footprint_name()}\"", "  (version 20240101)", "  (generator \"kicad-mil-ipc7351\")"]

        pkg = self.result.package
        if pkg:
            lines.append(f"  (descr \"{pkg.family} footprint generated by kicad-mil-ipc7351-footprint-generator\")")

        lines.extend(self._generate_pads())
        lines.extend(self._generate_courtyard())
        lines.append(")")

        return "\n".join(lines)

    def _footprint_name(self) -> str:
        pkg = self.result.package
        if pkg and pkg.body:
            return f"{pkg.family}_{pkg.body.length.nominal:.2f}x{pkg.body.width.nominal:.2f}_{self.result.density}"
        return "unnamed_footprint"

    def _generate_pads(self) -> list[str]:
        lines = []
        for i, pad in enumerate(self.result.pads):
            lines.append(f"  (pad {i+1} smd rect (at 0 {i * -1.5}) (size {pad.width:.4f} {pad.height:.4f}) (layers \"F.Cu\" \"F.Paste\" \"F.Mask\"))")
        return lines

    def _generate_courtyard(self) -> list[str]:
        if not self.result.courtyard:
            return []
        cy = self.result.courtyard
        return [
            f"  (fp_line (start {cy.x_min:.4f} {cy.y_min:.4f}) (end {cy.x_max:.4f} {cy.y_min:.4f}) (layer \"F.CrtYd\") (width 0.05))",
            f"  (fp_line (start {cy.x_max:.4f} {cy.y_min:.4f}) (end {cy.x_max:.4f} {cy.y_max:.4f}) (layer \"F.CrtYd\") (width 0.05))",
            f"  (fp_line (start {cy.x_max:.4f} {cy.y_max:.4f}) (end {cy.x_min:.4f} {cy.y_max:.4f}) (layer \"F.CrtYd\") (width 0.05))",
            f"  (fp_line (start {cy.x_min:.4f} {cy.y_max:.4f}) (end {cy.x_min:.4f} {cy.y_min:.4f}) (layer \"F.CrtYd\") (width 0.05))",
        ]

    def write_library(self, output_dir: str | Path, library_name: str = "generated") -> Path:
        """Create a .pretty folder and write the footprint inside."""
        lib_path = Path(output_dir) / f"{library_name}.pretty"
        lib_path.mkdir(parents=True, exist_ok=True)
        fp_path = lib_path / f"{self._footprint_name()}.kicad_mod"
        self.export(fp_path)
        return lib_path
